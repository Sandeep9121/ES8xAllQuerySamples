package com.elastic.demo.Controller;

public class Vehicle {

	private int year;
	private String make;
	private String model;
	private String engine;
	private String applicationSearchKey;
	private String type;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getEngine() {
		return engine;
	}

	public void setEngine(String engine) {
		this.engine = engine;
	}

	public String getApplicationSearchKey() {
		return applicationSearchKey;
	}

	public void setApplicationSearchKey(String applicationSearchKey) {
		this.applicationSearchKey = applicationSearchKey;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}


}
